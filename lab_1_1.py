# Задание 1.1 п.3 Считать с клавиатуры вещественное число m.
# Посчитать и вывести в консоль каждый член последовательности.
# Считывание вещественного числа с клавиатуры
m = float(input("Задание 1.1. п.3 - Введите вещественное число (m): "))

# Список для хранения элементов последовательности
sequence = []

# Цикл от 1 до 10
for i in range(1, 11):
    # Добавление элемента, умножая m на i
    sequence.append(m * i)

# Вывод списока элементов последовательности
for element in sequence:
    print(element)

