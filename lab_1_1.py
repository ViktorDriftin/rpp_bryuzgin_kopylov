# Задание 1.1 п.4 Считать с клавиатуры непустую произвольную последовательность целых чисел.
# Считывание произвольной последовательности целых чисел с клавиатуры
imput_numbers = input("Задание 1.1. п.4 - Введите последовательность чисел: ")

# Разбитие строки на отдельные числа
sequence_list = imput_numbers.split()

# Инициализация переменных для подсчета суммы и количества чисел
sum = 0
count = 0

# Цикл с выполнением до тех пор, пока в есть элементы
while len(sequence_list) > 0:
    # Извлечение первого элемента и преобразование в целое число
    num = int(sequence_list.pop(0))
    # Увеличение суммы на значение числа
    sum += num
    # Повышение на 1
    count += 1

# Вывод результатов
print("Сумма всех чисел: ", sum)
print("Количество всех чисел: ", count)
